"""
Math functions
"""
#Random Variables for use
a = 3
b = -6
c = 9
# Addition / Subtraction
print (a + b)
print (a - b)
# Multiplication / division
print (a * b)
print (a / b)
# Exponents
print (a**b) #a to the power of b
print (b**a)
#Square Root / Cube root
print (c*(1/2))
print (c**.5)
print (c**(1/3))
#Modulus (%) -- Only returns the remainder AFTER division (basically divide and then gives remainder)
print (c%4)